package entity;

/**
 *
 * @author user
 */
public class Shoe {
    private String name;
    private String brand;
    private int price;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
    @Override
    public String toString() {
        return "Shoe{"
                + "name=" + name
                + ",\n brand=" + brand
                + ",\n price=" + price
                + "\n}";
    }
}
